package com.hotel.entity;

public class Rates {
    private Double regularWeekDays;
    private Double regularWeekend;
    private Double rewardsWeekDays;
    private Double rewardsWeekend;

    public Double getRegularWeekDays() {
        return regularWeekDays;
    }

    public void setRegularWeekDays(Double regularWeekDays) {
        this.regularWeekDays = regularWeekDays;
    }

    public Double getRegularWeekend() {
        return regularWeekend;
    }

    public void setRegularWeekend(Double regularWeekend) {
        this.regularWeekend = regularWeekend;
    }

    public Double getRewardsWeekDays() {
        return rewardsWeekDays;
    }

    public void setRewardsWeekDays(Double rewardsWeekDays) {
        this.rewardsWeekDays = rewardsWeekDays;
    }

    public Double getRewardsWeekend() {
        return rewardsWeekend;
    }

    public void setRewardsWeekend(Double rewardsWeekend) {
        this.rewardsWeekend = rewardsWeekend;
    }
}
